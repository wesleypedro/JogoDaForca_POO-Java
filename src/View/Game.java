    private void InCharKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_InCharKeyReleased
        String Char;
        int Size;
        if(InChar.getText().length() > 1){
            Char = InChar.getText();
            Size = Char.length();
            
            InChar.setText(Char.substring(Size - 1, Size));
            
            
            if(evt.getKeyCode() == KeyEvent.VK_ENTER){
                Game g = new Game();
                g.setVisible(true);
                g.setLocationRelativeTo(null);
            }
        }
        else{
            //BtnStart.setEnabled(false);
        }
    }//GEN-LAST:event_InCharKeyReleased

