    private void InNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_InNameKeyReleased
        //CController cC = new CController();
        CController cC = ObjectManager.getInstance();
        
        if(InName.getText().trim().length() > 0){
            BtnStart.setEnabled(true);
        }
        else{
            BtnStart.setEnabled(false);
        }
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            cC.Enter(InName.getText().trim());
        }
    }//GEN-LAST:event_InNameKeyReleased

